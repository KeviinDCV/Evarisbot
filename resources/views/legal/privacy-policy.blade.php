        <h1>Política de Privacidad</h1>
        <p class="last-updated">Última actualización: {{ date('d/m/Y') }}</p>

        <h2>1. Introducción</h2>
        <p>
            Hospital Universitario del Valle "Evaristo Garcia" (en adelante, "el Hospital", "nosotros" o "nuestro") se compromete a proteger la privacidad y seguridad de la información personal de nuestros usuarios. Esta Política de Privacidad describe cómo recopilamos, usamos, almacenamos y protegemos la información que usted nos proporciona a través de nuestro servicio de atención por WhatsApp Business.
        </p>

        <h2>2. Información que Recopilamos</h2>
        <p>Cuando usted se comunica con nosotros a través de WhatsApp, podemos recopilar:</p>
        <ul>
            <li>Número de teléfono de WhatsApp</li>
            <li>Nombre de perfil de WhatsApp</li>
            <li>Contenido de los mensajes que usted nos envía</li>
            <li>Fecha y hora de las comunicaciones</li>
            <li>Información que usted voluntariamente nos proporcione (consultas médicas, citas, información de contacto)</li>
        </ul>

        <h2>3. Uso de la Información</h2>
        <p>Utilizamos la información recopilada para:</p>
        <ul>
            <li>Proporcionar atención y soporte a pacientes y familiares</li>
            <li>Responder a consultas sobre servicios médicos</li>
            <li>Coordinar citas y seguimientos médicos</li>
            <li>Mejorar la calidad de nuestros servicios de atención al cliente</li>
            <li>Cumplir con obligaciones legales y regulatorias del sector salud</li>
        </ul>

        <h2>4. Base Legal para el Tratamiento</h2>
        <p>
            El tratamiento de sus datos personales se realiza bajo las siguientes bases legales:
        </p>
        <ul>
            <li>Consentimiento informado para el tratamiento de datos de salud</li>
            <li>Ejecución de servicios de atención médica solicitados</li>
            <li>Cumplimiento de obligaciones legales en el sector salud</li>
            <li>Ley 1581 de 2012 (Ley de Protección de Datos Personales de Colombia)</li>
        </ul>

        <h2>5. Protección de Datos</h2>
        <p>Implementamos medidas de seguridad técnicas y organizativas para proteger su información:</p>
        <ul>
            <li>Encriptación de datos sensibles</li>
            <li>Acceso restringido solo a personal autorizado</li>
            <li>Almacenamiento seguro en servidores protegidos</li>
            <li>Capacitación continua del personal en protección de datos</li>
            <li>Auditorías periódicas de seguridad</li>
        </ul>

        <h2>6. Compartir Información</h2>
        <p>
            NO vendemos, alquilamos ni compartimos su información personal con terceros para fines comerciales. Solo compartimos información cuando:
        </p>
        <ul>
            <li>Es necesario para la prestación del servicio médico solicitado</li>
            <li>Existe una obligación legal o requerimiento judicial</li>
            <li>Se cuenta con su consentimiento explícito</li>
        </ul>

        <h2>7. Retención de Datos</h2>
        <p>
            Conservamos su información personal solo durante el tiempo necesario para cumplir con los fines para los cuales fue recopilada y conforme a los requisitos legales aplicables en el sector salud. Los datos médicos se conservan según lo establecido por la normativa sanitaria colombiana.
        </p>

        <h2>8. Sus Derechos</h2>
        <p>Usted tiene derecho a:</p>
        <ul>
            <li>Acceder a sus datos personales</li>
            <li>Rectificar datos inexactos o incompletos</li>
            <li>Solicitar la eliminación de sus datos (derecho al olvido)</li>
            <li>Oponerse al tratamiento de sus datos</li>
            <li>Solicitar la portabilidad de sus datos</li>
            <li>Revocar el consentimiento en cualquier momento</li>
        </ul>

        <h2>9. Uso de WhatsApp Business API</h2>
        <p>
            Utilizamos WhatsApp Business API proporcionada por Meta Platforms, Inc. para gestionar nuestras comunicaciones. Meta puede procesar ciertos datos según su propia política de privacidad, disponible en: <a href="https://www.whatsapp.com/legal/privacy-policy" target="_blank">https://www.whatsapp.com/legal/privacy-policy</a>
        </p>

        <h2>10. Menores de Edad</h2>
        <p>
            Nuestros servicios están dirigidos a personas mayores de edad o a sus representantes legales. Si somos conscientes de que hemos recopilado datos de menores sin consentimiento parental, tomaremos medidas para eliminar esa información.
        </p>

        <h2>11. Cambios a esta Política</h2>
        <p>
            Nos reservamos el derecho de modificar esta Política de Privacidad en cualquier momento. Los cambios serán efectivos inmediatamente después de su publicación. Le recomendamos revisar periódicamente esta página.
        </p>

